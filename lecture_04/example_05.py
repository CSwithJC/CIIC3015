def print_all_characters_in_a_word(word):
    print(f"\nPrinting all characters of {word}:")
    for char in word:
        print(char)

# Use a for loop to iterate over a string and print each character.
string_1 = "Colegio"
string_2 = "Puerto Rico"
string_3 = "Programming is awesome!"

print_all_characters_in_a_word(string_1)
print_all_characters_in_a_word(string_2)
print_all_characters_in_a_word(string_3)